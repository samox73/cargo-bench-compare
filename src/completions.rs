pub fn run(args: &CompletionsArgs) -> Result<()> {
    let content = script(args.shell)?;
    if args.install {
        return install(args.shell, &content);
    }
    std::io::stdout().write_all(content.as_bytes())?;
    Ok(())
}

fn script(shell: CompletionShell) -> Result<String> {
    if matches!(shell, CompletionShell::Nushell) {
        return Ok(NU_MODULE.to_owned());
    }
    let clap_shell = match shell {
        CompletionShell::Bash => clap_complete::Shell::Bash,
        CompletionShell::Zsh => clap_complete::Shell::Zsh,
        CompletionShell::Fish => clap_complete::Shell::Fish,
        CompletionShell::Elvish => clap_complete::Shell::Elvish,
        CompletionShell::Powershell => clap_complete::Shell::PowerShell,
        CompletionShell::Nushell => unreachable!(),
    };
    let mut out = Vec::new();
    clap_complete::generate(
        clap_shell,
        &mut crate::cli::command(),
        "cargo-bench-compare",
        &mut out,
    );
    Ok(String::from_utf8(out)?)
}

fn install(shell: CompletionShell, content: &str) -> Result<()> {
    let path = match shell {
        CompletionShell::Nushell => xdg_data_home()
            .join("nushell")
            .join("vendor")
            .join("autoload")
            .join("cargo-bench-compare.nu"),
        CompletionShell::Bash => xdg_data_home()
            .join("bash-completion")
            .join("completions")
            .join("cargo-bench-compare"),
        CompletionShell::Fish => home_dir()?
            .join(".config")
            .join("fish")
            .join("completions")
            .join("cargo-bench-compare.fish"),
        CompletionShell::Zsh => home_dir()?.join(".zfunc").join("_cargo-bench-compare"),
        CompletionShell::Elvish | CompletionShell::Powershell => {
            return Err(anyhow!(
                "--install is not supported for {}. Write this script to your shell's completion path manually: cargo-bench-compare completions {} > <path>",
                shell_name(shell),
                shell_name(shell)
            ));
        }
    };

    if let Some(parent) = path.parent() {
        std::fs::create_dir_all(parent)?;
    }
    std::fs::write(&path, content)?;
    println!("wrote {}", path.display());
    match shell {
        CompletionShell::Nushell => println!("restart nushell"),
        CompletionShell::Bash => println!("bash-completion v2 will lazy-load this by command name"),
        CompletionShell::Fish => println!("fish will load this automatically"),
        CompletionShell::Zsh => println!("add `fpath+=~/.zfunc` before `compinit` in ~/.zshrc"),
        CompletionShell::Elvish | CompletionShell::Powershell => unreachable!(),
    }
    Ok(())
}

fn xdg_data_home() -> PathBuf {
    std::env::var_os("XDG_DATA_HOME")
        .map(PathBuf::from)
        .unwrap_or_else(|| {
            home_dir()
                .unwrap_or_else(|_| PathBuf::from("."))
                .join(".local")
                .join("share")
        })
}

fn home_dir() -> Result<PathBuf> {
    std::env::var_os("HOME")
        .map(PathBuf::from)
        .ok_or_else(|| anyhow!("HOME is unset"))
}

fn shell_name(shell: CompletionShell) -> &'static str {
    match shell {
        CompletionShell::Bash => "bash",
        CompletionShell::Zsh => "zsh",
        CompletionShell::Fish => "fish",
        CompletionShell::Elvish => "elvish",
        CompletionShell::Powershell => "powershell",
        CompletionShell::Nushell => "nushell",
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn nu_module_covers_all_long_flags() {
        let cmd = crate::cli::command();
        assert_nu_module_covers_long_flags(&cmd);
    }

    fn assert_nu_module_covers_long_flags(cmd: &clap::Command) {
        for arg in cmd.get_arguments() {
            if let Some(long) = arg.get_long() {
                if long == "version" {
                    continue;
                }
                assert!(
                    NU_MODULE.contains(&format!("--{long}")),
                    "nu module missing --{long}"
                );
            }
        }
        for sub in cmd.get_subcommands() {
            assert_nu_module_covers_long_flags(sub);
        }
    }

    #[test]
    fn nu_module_exports_nested_commands_for_scoped_help() {
        for needle in [
            "nu-complete bcmp shells",
            r#"["bash", "zsh", "fish", "elvish", "powershell", "nushell"]"#,
            r#"def "nu-complete bcmp nothing" [] { [] }"#,
            r#"subcommand?: string@"nu-complete bcmp nothing""#,
            r#"export extern "cargo bench-compare cache""#,
            r#"export extern "cargo bench-compare cache list""#,
            r#"export extern "cargo bench-compare cache clean""#,
            r#"export extern "cargo-bench-compare cache""#,
            r#"export extern "cargo-bench-compare cache list""#,
            r#"export extern "cargo-bench-compare cache clean""#,
            "--all",
        ] {
            assert!(
                NU_MODULE.contains(needle),
                "nu module missing completion helper {needle}"
            );
        }

        // subcommand names come from nushell's native known-external completion on the
        // nested externs; a completer that returned them again would duplicate every
        // entry, while no completer at all would fall back to file-path completion
        for needle in [
            "  command?: string",
            "cache-subcommands",
            r#"str contains " cache ""#,
        ] {
            assert!(
                !NU_MODULE.contains(needle),
                "nu module should not use catch-all subcommand positional {needle}"
            );
        }
    }
}
